"""
trajectory_schema.py — Phase 0 of the medical-JEPA roadmap.

Defines the canonical "patient state trajectory" format that ALL future
medical-JEPA work trains on, plus a validator. Write the schema first;
write models later. Anything that produces training data (MIMIC converter,
Osler synthetic generator, vignette snapshots) must emit THIS format.

Design goals:
  - Time-ordered events of mixed modality (vitals, labs, symptoms, meds).
  - Per-timepoint organ-state labels (the JEPA latent target) with a
    confidence + provenance, because most states are only weakly observable.
  - Outcomes for downstream supervised probes.
  - Strict separation of `source` so synthetic data can never be mistaken
    for real clinical data (clinical claims require real data).

This file has NO ML dependencies — pure stdlib. It is the contract.
"""

from __future__ import annotations
from dataclasses import dataclass, field, asdict
from typing import Optional
import json


# ──────────────────────────────────────────────────────────────────────
# Vocabularies — keep these explicit so the dataset is self-describing.
# ──────────────────────────────────────────────────────────────────────

EVENT_KINDS = {
    "vitals",        # {hr, bp_sys, bp_dia, spo2, temp, rr, ...}
    "labs",          # {WBC, CRP, creatinine, troponin, lactate, ...}
    "symptoms",      # ["cough", "fever", ...]  (list, not dict)
    "intervention",  # {drug, dose_mg, route} or {procedure}
    "imaging",       # {modality, finding}  e.g. {"modality":"CXR","finding":"RLL opacity"}
    "note",          # {text}  free-text clinical note (optional)
}

DATA_SOURCES = {
    "real_ehr",        # de-identified real patient data (MIMIC, etc.) — clinically valid
    "vignette",        # textbook / USMLE case — educational, single timepoint usually
    "synthetic_osler", # generated by Osler's simulator — PRETRAIN ONLY, NOT clinically valid
    "manual",          # hand-authored test case
}

# States Osler can directly observe from labs/vitals vs. only infer.
# (Populated from lab_integration.json by the converter; this is the seed.)
LABEL_SOURCES = {
    "lab_direct",      # state read directly from an abnormal lab — highest confidence
    "vital_direct",    # state read from a vital sign
    "mechanism",       # inferred by Osler's rules from symptoms/other states — weaker
    "ground_truth",    # known from outcome/imaging — for held-out eval only
}


# ──────────────────────────────────────────────────────────────────────
# Core dataclasses
# ──────────────────────────────────────────────────────────────────────

@dataclass
class Event:
    """One observation at one time. `values` shape depends on `kind`."""
    t_hours: float                 # hours since trajectory start (t0 = 0.0)
    kind: str                      # one of EVENT_KINDS
    values: object                 # dict for most kinds; list for "symptoms"

    def validate(self) -> list:
        errs = []
        if self.kind not in EVENT_KINDS:
            errs.append(f"event.kind '{self.kind}' not in {sorted(EVENT_KINDS)}")
        if not isinstance(self.t_hours, (int, float)):
            errs.append(f"event.t_hours must be numeric, got {type(self.t_hours).__name__}")
        if self.kind == "symptoms":
            if not isinstance(self.values, list):
                errs.append("symptoms event.values must be a list of strings")
        else:
            if not isinstance(self.values, dict):
                errs.append(f"{self.kind} event.values must be a dict")
        return errs


@dataclass
class StateLabel:
    """A weak/strong label for one organ state at one timepoint."""
    state: str                     # "lung.gas_exchange"
    value: float                   # 0..1 (Osler's directional magnitude)
    direction: str                 # "high" | "low" | "normal"
    source: str                    # one of LABEL_SOURCES
    confidence: float              # 0..1
    rationale: str = ""            # which observation produced this

    def validate(self) -> list:
        errs = []
        if "." not in self.state:
            errs.append(f"state '{self.state}' should be 'organ.variable'")
        if self.source not in LABEL_SOURCES:
            errs.append(f"label.source '{self.source}' not in {sorted(LABEL_SOURCES)}")
        if not (0.0 <= self.confidence <= 1.0):
            errs.append(f"label.confidence {self.confidence} out of [0,1]")
        return errs


@dataclass
class StateSnapshot:
    """All organ-state labels inferred at one timepoint."""
    t_hours: float
    labels: list = field(default_factory=list)  # list[StateLabel]

    def validate(self) -> list:
        errs = []
        if not isinstance(self.t_hours, (int, float)):
            errs.append("snapshot.t_hours must be numeric")
        for lab in self.labels:
            errs += [f"snapshot@{self.t_hours}: {e}" for e in lab.validate()]
        return errs


@dataclass
class Trajectory:
    """One patient's timeline + derived state snapshots + outcomes."""
    patient_id: str
    source: str                              # one of DATA_SOURCES
    demographics: dict = field(default_factory=dict)   # {age, sex, ...}
    events: list = field(default_factory=list)         # list[Event], time-ordered
    state_snapshots: list = field(default_factory=list)# list[StateSnapshot]
    outcomes: dict = field(default_factory=dict)       # {icu_admission, death_30d, discharge_dx, ...}
    meta: dict = field(default_factory=dict)           # anything extra (generator version, etc.)

    def validate(self) -> list:
        errs = []
        if not self.patient_id:
            errs.append("trajectory.patient_id is required")
        if self.source not in DATA_SOURCES:
            errs.append(f"trajectory.source '{self.source}' not in {sorted(DATA_SOURCES)}")
        # events must be time-ordered
        ts = [e.t_hours for e in self.events]
        if ts != sorted(ts):
            errs.append("events must be sorted by t_hours")
        for e in self.events:
            errs += e.validate()
        for s in self.state_snapshots:
            errs += s.validate()
        return errs

    # ── serialization ──
    def to_dict(self) -> dict:
        return {
            "patient_id": self.patient_id,
            "source": self.source,
            "demographics": self.demographics,
            "events": [asdict(e) for e in self.events],
            "state_snapshots": [
                {"t_hours": s.t_hours, "labels": [asdict(l) for l in s.labels]}
                for s in self.state_snapshots
            ],
            "outcomes": self.outcomes,
            "meta": self.meta,
        }

    @classmethod
    def from_dict(cls, d: dict) -> "Trajectory":
        return cls(
            patient_id=d.get("patient_id", ""),
            source=d.get("source", "manual"),
            demographics=d.get("demographics", {}),
            events=[Event(**e) for e in d.get("events", [])],
            state_snapshots=[
                StateSnapshot(
                    t_hours=s["t_hours"],
                    labels=[StateLabel(**l) for l in s.get("labels", [])],
                )
                for s in d.get("state_snapshots", [])
            ],
            outcomes=d.get("outcomes", {}),
            meta=d.get("meta", {}),
        )


# ──────────────────────────────────────────────────────────────────────
# Dataset-level helpers (JSONL — one trajectory per line)
# ──────────────────────────────────────────────────────────────────────

def write_jsonl(trajectories: list, path: str) -> int:
    """Write trajectories to a JSONL file. Returns count written."""
    n = 0
    with open(path, "w", encoding="utf-8") as f:
        for t in trajectories:
            f.write(json.dumps(t.to_dict(), ensure_ascii=False) + "\n")
            n += 1
    return n


def read_jsonl(path: str) -> list:
    """Read trajectories from a JSONL file."""
    out = []
    with open(path, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                out.append(Trajectory.from_dict(json.loads(line)))
    return out


def validate_dataset(trajectories: list) -> dict:
    """Validate every trajectory. Returns a report."""
    report = {"total": len(trajectories), "valid": 0, "invalid": 0, "errors": []}
    for i, t in enumerate(trajectories):
        errs = t.validate()
        if errs:
            report["invalid"] += 1
            report["errors"].append({"index": i, "patient_id": t.patient_id, "errors": errs})
        else:
            report["valid"] += 1
    return report


